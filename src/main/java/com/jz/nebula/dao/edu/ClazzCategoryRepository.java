package com.jz.nebula.dao.edu;

import com.jz.nebula.entity.edu.ClazzCategory;
import org.springframework.data.domain.Page;
import org.springframework.data.domain.Pageable;
import org.springframework.data.jpa.repository.Query;
import org.springframework.data.repository.PagingAndSortingRepository;
import org.springframework.data.repository.query.Param;

public interface ClazzCategoryRepository extends PagingAndSortingRepository<ClazzCategory, Long> {
    @Query("select c from ClazzCategory c where lower(c.name) like %?1%")
    Page<ClazzCategory> findByNameContaining(@Param("keyword") String keyword, Pageable pageable);
}
